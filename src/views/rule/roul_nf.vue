    <p >一般规则</p>

      <p>除非另有注明, 所有比赛至少要进行55分钟，投注的注单才被视为有效。 </p>
      <p>最终赛果是以全场四节的比分为准（包括加时赛）。 </p>
      <p>单节/半场的投注，必须在比赛赛节完成后注单才被视为有效。 </p>
