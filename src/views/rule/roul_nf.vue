    <p >投注类型</p>

      <p >独赢</p>

        <p>预测哪一支球队将在比赛胜出。盘口提供两支球队为投注选项。 </p>

      <p >让球</p>

        <p>预测哪一支球队在盘口指定的让分数赢得某个时节或全场比赛。 </p>

      <p >滚球让球</p>

        <p>预测哪一支球队在盘口指定的让分数赢得某个时节或全场比赛。 </p>
        <p>结算是以0-0的比分在比赛结束后按盘口开出的让分数做裁决。投注当时的比分对结算没有影响。 </p>

      <p >大/小（总比分）</p>

        <p>预测赛事总比分将大于或小于在盘口指定的大/小盘分数。 </p>

      <p >滚球大/小（总比分）</p>

        <p>预测赛事总比分将大于或小于在盘口指定的大/小盘分数。 </p>
        <p>结算是以0-0的比分在比赛结束后按盘口开出的让分数做裁决。投注当时的比分对结算没有影响。 </p>
      <p >单/双</p>

        <p>预测赛事的总比分是单数或双数。加时赛将包括在内。 </p>

      <p >半场/全场</p>

        <p>预测在全场四节的半全场优胜球队。加时赛将包括在内。 </p>
        <p>此盘口也被称为'双重结果'。 </p>

      <p >净胜分数</p>

        <p>投注的结算根据全场四节两支球队比分的差别做裁决。加时赛将包括在内。 </p>
        <p>如果比赛在任何时间中断，所有注单将被取消。 </p>

      <p >最先得分球队</p>

        <p>预测最先得分的球队。 </p>
        <p>如果赛事在有得分后中断，所有最先得分球队的注单将保持有效。 </p>
        <p>如果赛事在没有球队得分前中断，所有最先得分球队的注单将被取消。 </p>
        <p>如果赛事在4节完场时间以及加时赛内没有球队得分，所有最先得分球队的注单将被取消。 </p>
