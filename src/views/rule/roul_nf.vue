        <p>预测最后得分的球队。 </p>
        <p>如果赛事中断， 所有最后得分球队的注单将被取消。 </p>
        <p>如果赛事在4节完场时间以及加时赛内没有球队得分，所有最后得分球队的注单将被取消。 </p>
      <p >单节最高得分球队</p>

        <p>预测单节最高得分的球队。 </p>
        <p>加时赛不计算在内。 </p>
        <p>如果赛事中断，所有单节最高得分球队的注单将被取消。 </p>
        <p>如果赛事在4节完场时间内没有球队得分，所有单节最高得分球队的注单将被取消。 </p>
      <p >每节最先获得10分的球队</p>

        <p>预测每节最先得10分的球队。 </p>
        <p>加时赛不计算在内。 </p>
        <p>如果赛事中断，所有每节最先获得10分的球队的注单将被取消.</p>
        <p>如果每节都没有球队获得10分，所有注单将被取消。 </p>
        <p>取决于赛事，玩法指定球队需最先获得的分数可能有变化，并且会清楚的显示在盘口。 </p>
